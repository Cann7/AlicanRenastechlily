package Day_10StringMethods;

public class Topic3_ForLoop {
    public static void main(String[] args) {
        //loops will help you on continus actions

        //in the loop
        //you will always need to specify
        //starting point
        // ending point
        //iteration
        for (int i = 0; i < 20; i+=2) { // we put here i++ bcz its gonna be count 1 by 1

            System.out.println("Increase: " + i);

            
        }
        for (int j = 10; j > 0; j--) {
            System.out.println("Decrease : " + j);

        }

    }
}