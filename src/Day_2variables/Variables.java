package Day_2variables;

public class Variables {

    public static void main(String[] args) {
     // boolean, It is true or false. It is 1 bit,
     boolean bolvalue = true;
        System.out.println(bolvalue);
        bolvalue = false;
        System.out.println(bolvalue);

     // byte , It is 8 bit values between -128 to 127

     byte bytevlue = 120;
     System.out.println(bytevlue);

     // char come from character, it is 16 bytes, it contains characters, numbers and so on
     char charvalue = 'W';
     System.out.println(charvalue);

     // short it takes values -32768 to 32767
     short shortvalue = 20000;
     System.out.println(shortvalue);


     // int it is short or integers. 32 bits , -2 bil and 2 bil

     int intvalue = 40000;
     System.out.println(intvalue);

     // long 64 bits memory,

     long longvalue = 234875645l;
     System.out.println(longvalue);

     // float it is 32 bits it holds only 7 decimal digits

     float floatvalue = 123.7f;
     float floatvalue2 = 123.784522128f;
     System.out.println(floatvalue);
     System.out.println(floatvalue2);

     // double 64 bits hold up to 16 digits after decimal point.
     double doublevalue = 123.548654895;
     System.out.println(doublevalue);


     // none-primitive



    }
}
