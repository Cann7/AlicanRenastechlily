package Day_31_Abstraction.Interface;

public class InterfaceIntro {

    /*
 An interface is a completely "abstract class" that is used to group related methods with empty bodies.
    Interface methods does not have body To access the interface methods,
    the interface must be "implemented" (kinda like inherited) by another class with the implements keyword (instead of extends).
    The body of the interface method is provided by the "implement" class
Like abstract classes,
interfaces cannot be used to create objects (in the example above, it is not possible to create an "Animal")
Interface methods do not have a body - the body is provided by the "implement" class
On implementation of an interface, you must override all of its methods
Interface methods are by default abstract and public
Interface attributes are by default public, static and final
An interface cannot contain a constructor (as it cannot be used to create objects)

     */

}
